"""
Question -
Salesperson Rita drives 1052 miles in 6 days, stopping at 2 towns each day.
How many KM does she average b/w stops.
"""

miles_traveled = 1052
days = 6
each_day_miles_travelled = miles_traveled / days
stops_each_day = 2
km_avg_btw_stops = each_day_miles_travelled / stops_each_day

print(km_avg_btw_stops * 1.60934)
